# 1、turtle模块的使用
'''
    1、在cmd窗口下或者是python的自带ide窗口中，输入：
    import turtle
    turtle.showturtle()         出现一个窗口，并且有一个箭头
    turtle.write('O')           在画笔显示处，也就是原点，显示出写的内容
    turtle.forward(300)         向箭头指的方向移动指定的距离
    turtle.color('red')         把画笔变成指定的颜色(注意:常见的颜色都可以)
    turtle.left(90)             把箭头所指的位置向右转90度(注意：左右都可以)
    turtle.goto(0,50)           去坐标上的制定点(注意：指定点的坐标是常用的（x,y）)
    turtle.penup()              把画笔抬起来，也就是在移动的时候不显示出轨迹(抬笔)
    turtle.pendown()            把画笔落下去，也就是在移动的时候显示出轨迹(落笔)
    turtle.circle(100)          以半径为100画圆
'''















