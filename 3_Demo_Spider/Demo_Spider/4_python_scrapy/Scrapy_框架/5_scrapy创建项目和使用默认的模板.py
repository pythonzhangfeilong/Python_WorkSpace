
# 1、创建scrapy项目
'''
    在指定的文件夹下打开cmd窗口输入固定命令scrapy startproject 加项目名字
    找到创建项目的文件夹，在当前的文件夹下打开cmd窗口，输入scrapy startproject projectname
'''

# 2、使用scrapy固定的模板
'''
    使用cmd进入项目文件目录，输入scrapy genspider objectname 域名(不加http)
    scrapy genspider images image.so.com
'''

# 3、按照上面的操作创建的好的项目，在spiders文件夹下就会出现固定的模板，就不需要自己重新构写了
