# 类由一下部分组成
'''
关键字 :class
类的名称: 类名
类的属性: 一组数据
类的方法: 允许对进行操作的方法 (行为)
实例方法：允许对象操作的方法（行为）
'''

# 类的作用
'''
类是为了描述对象的行为和属性
类也可以用于创建一个或者多个对象
'''

