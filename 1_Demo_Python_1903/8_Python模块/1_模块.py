#####模块:模块是一个包含一系列数据、函数、类等组成的程序组，模块是一个py文件，通常是.py结尾的

#####模块的作用：
'''
让一些相关的数据、函数、类等有逻辑的组织在一起，让逻辑结构更加清晰
模块中的数据、函数、类等可提供给其他的模块或者程序使用
'''

#####模块的分类:
'''
内建模块：在解析器内可以直接使用
标准库模块：安装python时已安装，并且可以直接使用的模块，
第三方模块：需要自己安装
自己编写的模块可以作为其他人员的第三方模块
'''

#####模块的导入语句：import
