#####布尔值：bool   （也就是只有列个结果True和False）

#####布尔值可以用与逻辑运算符（非not 且and 或or），执行顺序也是：非且或
# not（非）  也可理解为反义
# and（且）所有的条件符合才是True
# or（或）只要其中一个为True都是True

#####空值就是None  （None不能理解为0，0是有意义的，None是一个特殊的空值）

