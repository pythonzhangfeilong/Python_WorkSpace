#####文件：简单的说就是文本文件，复杂点的说就是超文本文件、图片、声音、超链接、视屏
'''
python中使用open打开文件

想要打开或者写入数据都要使用with来完成
'''

#####语法：
'''
File object=open(path,mode,[buffering])
open文件路径，也可以使用os模块来获取文件路径
mode权限，r(read读)w(write写)a(add追加)
buffering是否开启缓存（系统默认的是全缓存-1，0是不启用缓存，1是启用缓存）
'''

#####文件的作用：
'''把一些数据存放起来，可以让程序下一次执行的时候直接使用，而不需要重新制作一份，省时省力'''





















