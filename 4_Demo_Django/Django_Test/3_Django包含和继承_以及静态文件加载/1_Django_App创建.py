#####Django_App的创建(命令行的方式：)
'''
    1、将命令行切换到项目的根目录
    2、使用命令创建App
        python manage.py startapp Appname
        python manage.py startapp是固定的写法
        Appname也就是App的名字
'''

#####Django_App的创建(PyCharm中Terminal创建：)
'''
    1、在PyCharm中找到Terminal打开，输入python manage.py startapp Appname
'''

#####Django_App的创建(PyCharm创建Django项目时创建App：)
'''
                                ******使用的最多最实用*******
    1、直接在More Settings中的Applocaltion name后面输入App的名字即可自动的在Django项目中创建一个App
'''